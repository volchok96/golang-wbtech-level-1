package main

import (
	"fmt"
	"math"
	"sort"
)

// Дана последовательность температурных колебаний:
// -25.4, -27.0 13.0, 19.0, 15.5, 24.5, -21.0, 32.5.
// Объединить данные значения в группы с шагом в 10 градусов.
// Последовательность в подмножноствах не важна.
// Пример: -20:{-25.0, -27.0, -21.0}, 10:{13.0, 19.0, 15.5}, 20: {24.5}, etc.

func main() {
	// Заданный массив температурных колебаний
	temperatures := []float64{-25.4, -27.0, 13.0, 19.0, 15.5, 24.5, -21.0, 32.5}

	// Создаем карту для группировки температур, ключ — группа с шагом 10 градусов
	groups := make(map[int][]float64)

	// Проходим по каждой температуре в массиве
	for _, temp := range temperatures {
		var key int
		if temp < 0 {
			// Для отрицательных температур используем округление вверх
			key = int(math.Ceil(temp/10.0)) * 10
		} else {
			// Для положительных температур используем округление вниз
			key = int(math.Floor(temp/10.0)) * 10
		}
		// Добавляем температуру в соответствующую группу
		groups[key] = append(groups[key], temp)
	}

	// Получаем и сортируем ключи
	var keys []int
	for k := range groups {
		keys = append(keys, k)
	}
	sort.Ints(keys)

	// Выводим результат на экран в требуемом формате
	for _, key := range keys {
		fmt.Printf("%d: {", key)
		temps := groups[key]
		for i, temp := range temps {
			if i > 0 {
				fmt.Print(", ") 
			}
			fmt.Printf("%.1f", temp) // Форматируем вывод для десятичных чисел
		}
		fmt.Println("}")
	}
}
